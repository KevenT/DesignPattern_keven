package com.keven.dp.chainofresponsibility;
/** 
* @author Keven  Email:tv2014@126.com
* @version 创建时间：2019年3月22日 下午1:33:12 
* 说明 :
* 有多个对象，每个对象持有对下一个对象的引用，这样就会形成一条链，请求在这条链上传递，
* 直到某一对象决定处理该请求。但是发出者并不清楚到底最终那个对象会处理该请求，
* 所以，责任链模式可以实现，在隐瞒客户端的情况下，对系统进行动态的调整。
* 命令只允许由一个对象传给另一个对象，而不允许传给多个对象。
*/
public class ChainOfResponsibilityTest {

	public static void main(String[] args) {
		
		MyHandler h1 = new MyHandler("a");
		MyHandler h2 = new MyHandler("b");
		MyHandler h3 = new MyHandler("c");
		
		h1.setHandler(h2);
		h2.setHandler(h3);
		
		h1.operator();
		

	}

}
